    2018;68:1 Abstract Although follow-up after open surgical and endovascular procedures is generally regarded as an
    important part of the care provided by vascular surgeons, there are no detailed or comprehensive guidelines that
    specify the optimal approaches with regard to testing methods, indications for reintervention, and follow-up
    intervals. To provide guidance to the vascular surgeon, the Clinical Practice Council of the Society for Vascular
    Surgery appointed an expert panel and a methodologist to review the current clinical evidence and to develop
    recommendations for follow-up after vascular surgery procedures. For those procedures for which high-quality
    evidence was not available, recommendations were based on observational studies, committee consensus, and indirect
    evidence. Recognizing that there are numerous published reports on the role of duplex ultrasound for surveillance of
    infrainguinal vein bypass grafts, the Society commissioned a systematic review and meta-analysis on this topic. The
    panel classified the strength of each recommendation and the corresponding quality of evidence on the basis of the
    Grading of Recommendations Assessment, Development, and Evaluation (GRADE) system: recommendations were graded
    either strong or weak, and the quality of evidence was graded high, moderate, or low. The resulting recommendations
    represent a wide variety of open surgical and endovascular procedures involving the extracranial carotid artery,
    thoracic and abdominal aorta, mesenteric and renal arteries, and lower extremity arterial revascularization. The
    panel also identified many areas in which there was a lack of high-quality evidence to support their
    recommendations. This suggests that there are opportunities for further clinical research on testing methods,
    threshold criteria, and the role of surveillance as well as on the modes of failure and indications for
    reintervention after vascular surgery procedures.
